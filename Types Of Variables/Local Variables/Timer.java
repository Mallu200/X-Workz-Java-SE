class Timer {
    public static void main(String[] args) {
        System.out.println("Main started");

        // Local variable: Stores the elapsed time for this session
        int secondsElapsed = 60;

        System.out.println("Timer status: Active");
        System.out.println("Time remaining: " + secondsElapsed + " seconds");

        System.out.println("Main ended");
    }
}